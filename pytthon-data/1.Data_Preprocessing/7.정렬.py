# 값 기준 정렬 오름차순 ------------------------------------------------------- 
# 판다스 변수명.sort_values(by=[ 칼럼명 ])
# 이 기준으로 오름차순으로 바꿀 수 있다

import pandas as pd
df = pd.DataFrame({'a' : [2, 3, 2, 7, 4], 'b' : [2, 1, 3, 5, 3], 'c' : [1, 1, 2, 3, 5]})
print(df,'\n')
df.sort_values(by = ['a'] , inplace = True)
print(df,'\n')

'''
   a  b  c
0  2  2  1
1  3  1  1
2  2  3  2
3  7  5  3
4  4  3  5 

a 칼럼 열의 값이 오름차순으로 바뀌었다.
   a  b  c
0  2  2  1
2  2  3  2
1  3  1  1
4  4  3  5
3  7  5  3 
'''


