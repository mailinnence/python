'''
시작전 몇 가지 중요 설정 
1. ascending 는 옵션이며 False 에서 내림차순  True 는 오름차순이다.
  ex) ---------------------------------------------------
       df.sort_index(ascending = False)  
       df.sort_index(ascending = True)
      ---------------------------------------------------

2. inplace = True 는 값을 저장하는 역할을 한다. False 가 default 값이며 
   설정하지 않을 시 따로 변수에 넣지 않는한 저장되지 않는다.
  ex) ---------------------------------------------------
      # 방법.1 
      df.reset_index(drop = True, inplace = True)
      print(df)

      # 방법.2
      a=df.reset_index(drop = True, inplace = True)
      print(a)
      print(df)   # 위 경우 설정이 저장되지 않았기 때문에 변화가 없다. 
      ---------------------------------------------------
'''






# 오름차순 (sort) --------------------------------------------------------
# 판다스 변수명.sort_index()  >>  인덱스를 오름차순으로 정렬시킴


import pandas as pd

df = pd.DataFrame({'a' : [2, 3, 2, 7, 4], 'b' : [2, 1, 3, 5, 3], 'c' : [1, 1, 2, 3, 5]})    
print(df,'\n')
print(df.sort_index(),'\n')

