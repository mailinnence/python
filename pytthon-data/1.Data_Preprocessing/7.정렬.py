# 초기화------------------------------------------------------------------------
# 판다스 변수명.reset_index() >> 인덱스만 초기화 됨 값은 바뀌지 않으니 주의!!!


import pandas as pd

df = pd.DataFrame({'a' : [2, 3, 2, 7, 4], 'b' : [2, 1, 3, 5, 3], 'c' : [1, 1, 2, 3, 5]})    
df.sort_index(ascending = False , inplace = True)
