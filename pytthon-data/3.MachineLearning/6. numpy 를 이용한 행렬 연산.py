# 배열간 사칙연산 -------------------------------------------------

import numpy as np
a = np.array([[1, 2, 3], [4, 5, 6]])
b = np.array([[2, 3, 4], [5, 6, 7]])

# ---------------------------------------------------
print(a + b ,'\n')
print(a + 3)

'''
[[ 3  5  7]
 [ 9 11 13]]

[[4 5 6]
 [7 8 9]]

'''


# ---------------------------------------------------
print(a - b,'\n')
print(a - 2)

'''
[[-1 -1 -1]
 [-1 -1 -1]]

[[-1  0  1]
 [ 2  3  4]]

'''


# ---------------------------------------------------
print(a * b,'\n')
print(a * 2)

'''
[[ 2  6 12]
 [20 30 42]] 

[[ 2  4  6]
 [ 8 10 12]]

'''


# ---------------------------------------------------
print(a / b, '\n')
