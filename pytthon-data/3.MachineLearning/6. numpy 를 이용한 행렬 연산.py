# 전치 행렬 (transposition matrix) --------------------------------
# 행렬 행과 열 뒤집기

import numpy as np
a = np.array([[1, 2, 3], [4, 5, 6]])

print(a , '\n')
print(a.T)

'''
행렬
[[1 2 3]
 [4 5 6]]

전치행렬
[[1 4]
 [2 5]
 [3 6]]

'''

