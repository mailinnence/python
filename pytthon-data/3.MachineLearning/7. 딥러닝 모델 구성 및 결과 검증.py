# 정규화
# 데이터의 최고 값을 보면 255 이다. 이를 255 로 나누어 주므로써
# 0~255가 아닌 0~1 까지로 학습에 하기 좋은 데이터로 바꾸어 준다.
x_train, x_test = x_train / 255.0, x_test / 255.0



# 그림 그리기
# 해당 숫자를 그림으로 바꾸어 주는 함수
plt.imshow(x_test[0])

# 맞는지 확인
y_test[0]





# 모델 작성
# Sequential 이라는 모델이용

model = tf.keras.models.Sequential([
# 28x28 한줄 자리 데이터가 입력됨
  tf.keras.layers.Flatten(input_shape=(28, 28)),

# 128 히든 레이어를 만들고 활성화 함수를 relu (해당 input 값이 0 이면 0을 출력 그외는 그대로 출력) 이용
  tf.keras.layers.Dense(128, activation='relu'),

# Dropout(0.2) 20프로 정도는 연결을 하지말고 부족하게 학습해라 왜?
# 너무 정확하면 정확해야만 판단을 한다.
  tf.keras.layers.Dropout(0.2),

# 10가지로 나눠서 softmax 를 이용
  tf.keras.layers.Dense(10, activation='softmax')
])

# 학습을 어떻게 할 것인가?
'''
활성화 함수는 adam 사용 , 
 loss 손실값을 어떻게 처리 할 것인가? ,
metrics=['accuracy'] 정확도

'''
model.compile(optimizer='adam',
              loss='sparse_categorical_crossentropy',
              metrics=['accuracy'])


# 모델 시각화
model.summary()



# 모델 학습 및 평가
# 5번 학습
model.fit(x_train, y_train, epochs = 5)



# 평가 verbose = n  
# n 은 형태의 유형
model.evaluate(x_test,  y_test, verbose = 2)



