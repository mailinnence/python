# 파이썬의 리스트를 반복문을 이용하여 좀 더 직관적으로 사용

# ex ----------------------------------------------------------------
a = [i for i in range(1, 11)]
print(a)

'''
a = [i for i in range(1, 11)]
a = [ a 는 i 인데 어떤 i 이냐면  for i in range(1, 11) 이다]
'''
# --------------------------------------------------------------------


# ex ----------------------------------------------------------------
a = [90, 39, 48, 70, 82, 100]
result = [i for i in a]
print(result)


'''
[90, 39, 48, 70, 82, 100]
'''
# --------------------------------------------------------------------




# ex ----------------------------------------------------------------
a = [90, 39, 48, 70, 82, 100]
result = [i for i in a if i >= 80]
print(result)

'''
[90, 82, 100]
'''
# --------------------------------------------------------------------




# ex ----------------------------------------------------------------
stu = ["a","b","c","d","e"]
sco = [80,95,90,86,90]

for i,v in zip(stu,sco):
    print("{}번의 이름은 {}".format(i,v))
    
    
    
'''
a번의 이름은 80
b번의 이름은 95
c번의 이름은 90
d번의 이름은 86
e번의 이름은 90
'''



